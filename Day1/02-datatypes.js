

//Primitive Datatypes:
/* 
1. number
2. string
3. Boolean
4. undefined
5. null */

/* Non Primitive Data types
1. arrays
2. Objects */

//number
var phoneNumber = 687.686746
console.log(phoneNumber);//6876867
console.log(typeof phoneNumber); // number


//string
var username = "support@testleaf.com"
console.log(username); //"support@testleaf.com"
console.log(typeof username);//string


//boolean
var validUsername = true;
console.log(validUsername); //true
console.log(typeof validUsername);//boolean



//undefined

var accountNumber
console.log(accountNumber); // undefined
console.log(typeof accountNumber);//undefined

//null

var middleName=null
console.log(middleName); // null
console.log(typeof middleName);//Object




